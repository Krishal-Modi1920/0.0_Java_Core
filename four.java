// Type Casting
public class four {
    public static void main(String[] args) {
        // Widening Type casting
        int i = 12;
        double d = i;

        System.out.println(d);

        // Narrowing Type Casting
        double dr = 12.78d;
        int myint = (int) dr;

        System.out.println(myint);
    }
}
